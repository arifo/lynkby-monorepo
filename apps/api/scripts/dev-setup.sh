# Check if wrangler.toml is in .gitignore
if grep -q "wrangler.toml" .gitignore; then
    echo "âœ… wrangler.toml is properly ignored by Git"
else
    echo "âŒ wrangler.toml is NOT ignored by Git - this is a security risk!"
fi

